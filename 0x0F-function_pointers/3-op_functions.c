/**
 * op_add - add two number
 * @a: number1
 * @b: number2
 *
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two number
 * @a: number1
 * @b: number2
 *
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two number
 * @a: number1
 * @b: number2
 *
 * Return: multiply
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two number
 * @a: number1
 * @b: number2
 *
 * Return: divide
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module two number
 * @a: number1
 * @b: number2
 *
 * Return: module
 */
int op_mod(int a, int b)
{
	return (a % b);
}
